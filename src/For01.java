
public class For01 {
	public static void main(String[] args) {
		/* for文の基本構文↓
		   for (初期化式; 条件式; 変化式) {
				ブロック(繰り返し処理)
			}
		 */
		
		for (int number = 1; number <= 5; number++) {
			System.out.println("For01 = " + number);
		}
		
		/* for文
		 * while文と同じように指定の条件が「true」の間、繰り返し処理を行なう制御文.
		 */
	}

}
